package com.entities;

import java.io.Serializable;
import java.util.Date;


/**
 * @author chglee
 * @email 1992lcg@163.com
 * @date 2018-09-29 13:41:07
 */
public class AreaInfo implements Serializable {
    private static final long serialVersionUID = 1L;

    //
    private Integer id;
    //
    private String name;
    //
    private Integer pid;
    //
    private Integer type;
    //
    private Integer areaLevStatus;
    //
    private Date createDate;
    //
    private Date updateDate;

    /**
     * 设置：
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取：
     */
    public Integer getId() {
        return id;
    }

    /**
     * 设置：
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * 获取：
     */
    public String getName() {
        return name;
    }

    /**
     * 设置：
     */
    public void setPid(Integer pid) {
        this.pid = pid;
    }

    /**
     * 获取：
     */
    public Integer getPid() {
        return pid;
    }

    /**
     * 设置：
     */
    public void setType(Integer type) {
        this.type = type;
    }

    /**
     * 获取：
     */
    public Integer getType() {
        return type;
    }

    /**
     * 设置：
     */
    public void setAreaLevStatus(Integer areaLevStatus) {
        this.areaLevStatus = areaLevStatus;
    }

    /**
     * 获取：
     */
    public Integer getAreaLevStatus() {
        return areaLevStatus;
    }

    /**
     * 设置：
     */
    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    /**
     * 获取：
     */
    public Date getCreateDate() {
        return createDate;
    }

    /**
     * 设置：
     */
    public void setUpdateDate(Date updateDate) {
        this.updateDate = updateDate;
    }

    /**
     * 获取：
     */
    public Date getUpdateDate() {
        return updateDate;
    }

    @Override
    public String toString() {
        return "AreaInfo{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", pid=" + pid +
                ", type=" + type +
                ", areaLevStatus=" + areaLevStatus +
                ", createDate=" + createDate +
                ", updateDate=" + updateDate +
                '}';
    }
}
